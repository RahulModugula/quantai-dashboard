    def run_equity_analysis(self, ticker: str = "AAPL") -> dict:
        """Run equity agent analysis with mock or live agents.

        Args:
            ticker: Stock ticker symbol

        Returns:
            Dictionary with analysis results
        """
        if self.use_mock:
            logger.info(f"Running equity analysis for {ticker} in MOCK mode")
            return self._get_mock_equity_result(ticker)
        else:
            logger.info(f"Running equity analysis for {ticker} in LIVE mode")
            from src.agents.orchestrator import run_full_analysis
            import asyncio

            result = asyncio.run(run_full_analysis(ticker))
            return {
                "ticker": ticker,
                "decision": result.get("decision", "HOLD"),
                "confidence": result.get("confidence", 0.5),
                "reasoning": result.get("reasoning", ""),
                "total_tokens": result.get("total_tokens", 0),
            }

