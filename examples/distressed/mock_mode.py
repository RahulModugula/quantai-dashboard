def run_demo(showcase: bool = True) -> None:
    """Run the demonstration showcase.

    This function runs both distressed credit and equity analyses
    in mock mode and prints the results.

    Args:
        showcase: If True, print formatted output. If False, return results.
    """
    print("\n" + "=" * 80)
    print("QUANTAI DASHBOARD - DEMONSTRATION SHOWCASE")
    print("=" * 80)
    print("\nMode: MOCK (no API key required)")
    print(f"Timestamp: {datetime.now().isoformat()}")
    print("\n" + "-" * 80)

    # Run ATI distressed credit analysis
    print("\n1. DISTRESSED CREDIT ANALYSIS - ATI PHYSICAL THERAPY")
    print("-" * 80)
    mock = MockCreditCommittee(use_mock=True)
    result = mock.run_ati_analysis()

    print(f"\nTotal LLM Tokens Used: {result.total_tokens:,}")
    print(f"\nCapStructureAgent: {result.cap_structure_brief.tokens_used:,} tokens")
    print(f"SituationAgent: {result.situation_brief.tokens_used:,} tokens")
    print(f"CreditRiskAgent: {result.credit_risk_brief.tokens_used:,} tokens")
    print(f"CreditCommitteeAgent: {result.committee_memo.tokens_used:,} tokens")

    # Print brief excerpts
    print("\n--- CAP STRUCTURE BRIEF EXCERPT ---")
    print(result.cap_structure_brief.content[:500] + "...")

    print("\n--- COMMITTEE RECOMMENDATION ---")
    lines = result.committee_memo.content.split("\n")
    for i, line in enumerate(lines[:15]):  # Show first 15 lines
        print(line)

    # Run equity analysis
    print("\n" + "-" * 80)
    print("\n2. EQUITY ANALYSIS - AAPL")
    print("-" * 80)
    equity_result = mock.run_equity_analysis("AAPL")

    print(f"\nTotal Tokens: {equity_result['total_tokens']:,}")
    print(f"\nDecision: {equity_result['decision']}")
    print(f"Confidence: {equity_result['confidence']:.0%}")

    print("\n--- PORTFOLIO MANAGER DECISION ---")
    print(equity_result["pm_brief"])

    print("\n" + "=" * 80)
    print("DEMONSTRATION COMPLETE")
    print("=" * 80)
    print("\nTo run live mode (requires API key):")
    print("  mock = MockCreditCommittee(use_mock=False)")
    print("  mock.run_ati_analysis()")
    print("\nTo run equity analysis live:")
    print("  mock.run_equity_analysis('AAPL')")
    print()


